-- para no duplicar tipos con el mismo nombre “normalizado” (acentos, paréntesis, etc.).
--
-- Comprobar:
--   select public.gym_normalize_tipo_nombre('Dominadas Pronas (Pull-ups)');
--
-- -----------------------------------------------------------------------------
-- Paso 1 — Renombrar tipos existentes
-- -----------------------------------------------------------------------------
-- Archivo: sql/renombrar_ejercicios_fitcron.sql
-- Fuente: sql/ejercicios_no_coinciden_con_sugerencia.csv (aplicar_rename = si).
-- Las filas con borrar = si NO entran aquí: el borrado manda y esas filas solo van
-- al script de borrado (paso 2), aunque tengan nombre_final o aplicar_rename.
-- Actualiza public.tipo_ejercicio.nombre por id (UUID fijo); el historial sigue enlazado
-- al mismo tipo_ejercicio_id.
--
-- Comprobar: select id, nombre from tipo_ejercicio order by nombre limit 20;
--
-- -----------------------------------------------------------------------------
-- Paso 2 — Borrar tipos marcados
-- -----------------------------------------------------------------------------
-- Archivo: sql/borrar_tipos_ejercicio_fitcron.sql
-- Fuente: mismo CSV (borrar = si). Obligatorio para todas esas filas, con o sin
-- aplicar_rename / nombre_final. Orden interno: serie → ejercicio →
-- rutina_ejercicio → tipo_ejercicio.
--
-- Comprobar: que los UUID del script ya no existan:
--   select id from tipo_ejercicio where id in ('...');
--
-- -----------------------------------------------------------------------------
-- Paso 3 — Insertar filas nuevas del catálogo FitCron
-- -----------------------------------------------------------------------------
-- Archivo: sql/insertar_ejercicios_fitcron.sql
-- Fuente: sql/ejercicios.csv. Inserta solo si no hay fila con el mismo
-- gym_normalize_tipo_nombre(nombre). Columnas: nombre (texto FitCron), descripcion,
-- usuario_id NULL, gif_url, body_part::text[], equipment, instructions::text[].
--
-- UUID: no se envía; usa default gen_random_uuid() en cada insert.
-- Tipos: text, text[], coherente con public.tipo_ejercicio en database.sql.
--
-- Comprobar recuento y muestra:
--   select count(*) from tipo_ejercicio where usuario_id is null;
--   select nombre, gif_url, body_part, instructions from tipo_ejercicio
--     where usuario_id is null order by nombre limit 5;
--
